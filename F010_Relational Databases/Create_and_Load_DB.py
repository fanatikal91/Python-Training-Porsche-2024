DBNAME = "orderdb.sqlite"

def convert_timestamp(timestamp : str) -> str:
    # Format des ursprünglichen Zeitstempels
    input_format = "%b %d %Y %I:%M:%S:%f%p"
    
    # Zeitstempel in das gewünschte Format parsen
    parsed_time = datetime.strptime(timestamp, input_format)

    # Formatieren in das Ziel-Format YYYY-MM-DD HH:MM:SS.SSS
    output_format = "%Y-%m-%d %H:%M:%S.%f"
    retVal = parsed_time.strftime(output_format)[:-3]  # Nur 3 Millisekunden-Stellen behalten
    # print(f"Org:  {timestamp}\t {retVal}")
    return retVal

def execute_sql_statements(conn : sqlite3.Connection, cmdList : list, success_txt : str) -> None:
    cursor = conn.cursor()

    try:
        # Ausführen der SQL-Befehle in einer Transaktion
        for cmd in cmdList:
            cursor.execute(cmd)
            # print(f"Ausgeführt: {cmd}")

        # Änderungen bestätigen
        conn.commit()
        
        if success_txt:
            print(success_txt)
    
    except sqlite3.Error as e:
        # Falls ein Fehler auftritt, Änderungen rückgängig machen
        conn.rollback()
        print(f"Fehler beim Ausführen der SQL-Befehle: {e}")

    finally:
        # Verbindung schließen
        # conn.close()
        if cursor:
            cursor.close()
            
