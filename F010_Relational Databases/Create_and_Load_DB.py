def get_create_tables_cmds() -> list:
    create_tables = []

    # Drop Tables first
    create_tables.append("DROP TABLE IF EXISTS Customers")
    create_tables.append("DROP TABLE IF EXISTS Orders")
    create_tables.append("DROP TABLE IF EXISTS OrderItems")
    create_tables.append("DROP TABLE IF EXISTS Products")
    create_tables.append("DROP TABLE IF EXISTS Suppliers")

    # Table:s
    create_tables.append(
        """
        CREATE TABLE Customers (
           Id INTEGER PRIMARY KEY,
           FirstName TEXT NOT NULL,
           LastName TEXT NOT NULL,
           City TEXT,
           Country TEXT,
           Phone TEXT
        )"""
    )

    # Table: Orders
    create_tables.append(
        """CREATE TABLE Orders (
           Id INTEGER PRIMARY KEY,
           OrderDate TEXT DEFAULT CURRENT_TIMESTAMP NOT NULL,
           OrderNumber TEXT,
           CustomerId INTEGER NOT NULL,
           TotalAmount REAL DEFAULT 0.0,
           FOREIGN KEY (CustomerId) REFERENCES Customers(Id)
        )"""
    )

    # Table: OrderItems
    create_tables.append(
        """CREATE TABLE OrderItems (
           Id INTEGER PRIMARY KEY,
           OrderId INTEGER NOT NULL,
           ProductId INTEGER NOT NULL,
           UnitPrice REAL DEFAULT 0.0 NOT NULL,
           Quantity INTEGER DEFAULT 1 NOT NULL,
           FOREIGN KEY (OrderId) REFERENCES Orders(Id),
           FOREIGN KEY (ProductId) REFERENCES Products(Id)
        )"""
    )

    # Table: Products
    create_tables.append(
        """CREATE TABLE Products (
           Id INTEGER PRIMARY KEY,
           ProductName TEXT NOT NULL,
           SupplierId INTEGER NOT NULL,
           UnitPrice REAL DEFAULT 0.0,
           Package TEXT,
           IsDiscontinued INTEGER DEFAULT 0 NOT NULL,
           FOREIGN KEY (SupplierId) REFERENCES Suppliers(Id)
        )"""
    )

    # Table: Suppliers
    create_tables.append(
        """CREATE TABLE Suppliers (
           Id INTEGER PRIMARY KEY,
           CompanyName TEXT NOT NULL,
           ContactName TEXT,
           ContactTitle TEXT,
           City TEXT,
           Country TEXT,
           Phone TEXT,
           Fax TEXT
        )"""
    )

    return create_tables

